package net.ck.mtbg.map;

/**
 * shorthand to translate from
 * ['sw sw sw sw sw sw sw sw sw gc gc sw sw sw sw sw sw sw sw sw'],
 * to
 * StoneWall Maptiles with two closed gates in the middle.
 *
 * <a href="https://www.baeldung.com/java-convert-enums#:~:text=When%20converting%20between%20enums%20with,to%20declare%20the%20enum%20constant">
 * https://www.baeldung.com/java-convert-enums#:~:text=When%20converting%20between%20enums%20with,to%20declare%20the%20enum%20constant
 * </a>.
 */
public enum MapShortHand
{
    ds, gs, hl, ld, mt, oc, re, rn, rr, rs, rw, su, sw, lu, sd, cw, ve, ce, te, ws, ww, wi, go, gc, wd, wc, sc, fn, we, dr, pr, wf, sf, mf, df, lf, so, rf, bs, lv, sm, sp,
}
